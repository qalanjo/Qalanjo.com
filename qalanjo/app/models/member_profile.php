	function getAge($member_id){
		$member_id = mysql_escape_string($member_id);
		$sql = "SELECT (DATE_FORMAT( FROM_DAYS( TO_DAYS( now( ) ) - TO_DAYS(birthdate) ) , '%Y' ) +0) AS age FROM member_profiles AS MemberProfile WHERE MemberProfile.member_id = $member_id";
		return $this->query($sql);		
	}
	
	function getAgeV2($member_id){
		$res = $this->getAge($member_id);
		if (isset($res[0][0])){
			return $res[0][0]["age"];	
		}
		return 0;
		
	}
	
	function getPictureOnly($member_id){
		$picture = $this->find("first", array("conditions"=>array("MemberProfile.member_id"=>$member_id),
